"""
Phase 5 — Generate markdown report from LangSmith experiment results.

Usage:
    python -m eval.reporter --experiment "H-agent_eval-<timestamp>"
    python -m eval.reporter --latest   # auto-pick most recent experiment
"""
import argparse
import json
from collections import defaultdict
from datetime import datetime
from pathlib import Path

from dotenv import load_dotenv

load_dotenv()

from langsmith import Client

RESULTS_DIR = Path("eval/results")
METRICS = ["task_completion", "tool_correctness", "response_quality", "routing_accuracy", "no_loop"]
METRIC_LABELS = {
    "task_completion": "A — Task Completion",
    "tool_correctness": "C — Tool Correctness",
    "response_quality": "D — Response Quality",
    "routing_accuracy": "B — Routing Accuracy",
    "no_loop": "E — No Loop",
}


def _get_experiment_name(client: Client, latest: bool, experiment: str | None) -> str:
    if experiment:
        return experiment
    projects = sorted(
        [p for p in client.list_projects() if p.name.startswith("H-agent_eval")],
        key=lambda p: p.modified_at or p.created_at,
        reverse=True,
    )
    if not projects:
        raise ValueError("No H-agent_eval experiments found in LangSmith")
    return projects[0].name


def generate_report(experiment_name: str) -> Path:
    client = Client()
    print(f"[reporter] Fetching results for experiment: {experiment_name}")

    runs = list(client.list_runs(
        project_name="H-agent_eval",
        execution_order=1,
        filter=f'has(tags, "{experiment_name}")',
    ))

    if not runs:
        # Fallback: list all runs in project
        runs = list(client.list_runs(project_name="H-agent_eval", execution_order=1))

    # Aggregate scores per metric
    scores: dict[str, list[float]] = defaultdict(list)
    failures: list[dict] = []

    for run in runs:
        if not run.feedback_stats:
            continue
        for metric_key, stats in run.feedback_stats.items():
            avg = stats.get("avg")
            if avg is not None:
                scores[metric_key].append(avg)
                if avg < 0.7:
                    failures.append({
                        "run_id": str(run.id),
                        "metric": metric_key,
                        "score": avg,
                        "input": str(run.inputs)[:120],
                    })

    # Build report
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    out_dir = RESULTS_DIR / timestamp
    out_dir.mkdir(parents=True, exist_ok=True)

    aggregate = {}
    report_lines = [
        f"# Eval Report — {experiment_name}",
        f"\n**Generated:** {datetime.now().strftime('%Y-%m-%d %H:%M')}",
        f"**Experiment:** {experiment_name}",
        f"**Total runs fetched:** {len(runs)}\n",
        "---\n",
        "## Scores per Dimension\n",
        "| Metric | Pass Rate | Mean Score | N |",
        "|---|---|---|---|",
    ]

    for key in METRICS:
        label = METRIC_LABELS.get(key, key)
        vals = scores.get(key, [])
        if vals:
            mean = sum(vals) / len(vals)
            pass_rate = sum(1 for v in vals if v >= 0.7) / len(vals)
            aggregate[key] = {"mean": round(mean, 3), "pass_rate": round(pass_rate, 3), "n": len(vals)}
            report_lines.append(f"| {label} | {pass_rate:.0%} | {mean:.2f} | {len(vals)} |")
        else:
            aggregate[key] = {"mean": None, "pass_rate": None, "n": 0}
            report_lines.append(f"| {label} | N/A | N/A | 0 |")

    if failures:
        report_lines += [
            "\n---\n",
            "## Failures (score < 0.7)\n",
            "| Metric | Score | Input |",
            "|---|---|---|",
        ]
        for f in failures[:20]:  # cap at 20
            report_lines.append(f"| {f['metric']} | {f['score']:.2f} | {f['input']} |")
    else:
        report_lines.append("\n---\n\n**No failures (all scores ≥ 0.7)** ✅")

    report_lines += [
        "\n---\n",
        f"_Report generated by `eval/reporter.py` — experiment `{experiment_name}`_",
    ]

    report_path = out_dir / "report.md"
    report_path.write_text("\n".join(report_lines))

    aggregate_path = out_dir / "aggregate.json"
    aggregate_path.write_text(json.dumps({"experiment": experiment_name, "metrics": aggregate}, indent=2))

    print(f"[reporter] Report saved to {report_path}")
    print(f"[reporter] Aggregate saved to {aggregate_path}")
    return report_path


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--experiment", type=str, default=None)
    parser.add_argument("--latest", action="store_true")
    args = parser.parse_args()

    client = Client()
    name = _get_experiment_name(client, args.latest, args.experiment)
    generate_report(name)
